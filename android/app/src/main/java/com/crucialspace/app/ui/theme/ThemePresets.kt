        return when (name) {
            "purple" -> PurpleExpressive
            "blue" -> OceanBlue
            "green" -> ForestGreen
            "orange" -> SunsetOrange
            "red" -> CherryRed
            "lavender" -> LavenderDream
            "midnight" -> Midnight
            else -> PurpleExpressive // Default fallback
        }
    }
    
